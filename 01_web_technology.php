<p>In computing, a web application or web app is a client-server software application in which the 
client (or user interface) runs in a web browser.</p>

<p>Web applications are popular due to the ubiquity of web browsers, and the convenience of using 
a web browser as a client to update and maintain web applications without distributing and 
installing software on potentially thousands of client computers is a key reason for their 
popularity, as is the inherent support for cross-platform compatibility. Common web applications 
include webmail, online retail sales, online auctions, wikis, instant messaging services and many 
other functions</p>